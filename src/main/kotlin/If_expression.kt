    val nilai = 72

    if (nilai >= 90) {
        println("Nilai Anda adalah A")
    } else if (nilai >= 80) {
        println("Nilai Anda adalah B")
    } else if (nilai >= 70) {
        println("Nilai Anda adalah C")
